local function DPMUYKL_fake_script() -- TextLabel_27.LocalScript 
	local script = Instance.new('LocalScript', TextLabel_27)

	local Players = game:GetService("Players")
	local player = Players.LocalPlayer
	local label = script.Parent
	
	local kills = 0
	label.Text = "Kills: " .. kills
	
	-- Track kills by detecting when a Humanoid dies with you as the creator
	local function onCharacterAdded(char)
		local humanoid = char:WaitForChild("Humanoid", 5)
		if not humanoid then return end
	
		humanoid.Died:Connect(function()
			local tag = humanoid:FindFirstChild("creator")
			if tag and tag.Value == player then
				kills += 1
			end
		end)
	end
	
	-- Hook into existing + future players
	for _, plr in ipairs(Players:GetPlayers()) do
		if plr ~= player then
			if plr.Character then onCharacterAdded(plr.Character) end
			plr.CharacterAdded:Connect(onCharacterAdded)
		end
	end
	
	Players.PlayerAdded:Connect(function(plr)
		if plr ~= player then
			plr.CharacterAdded:Connect(onCharacterAdded)
		end
	end)
	
	-- Update text every 0.1s
	task.spawn(function()
		while true do
			label.Text = "Kills: " .. kills
			task.wait(0.1)
		end
	end)
	
end
